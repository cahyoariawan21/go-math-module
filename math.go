package math

func Addition(a, b float64) float64 {
	result := a + b
	return result
}

func Subtraction(a, b float64) float64 {
	result := a - b
	return result
}

func Distribution(a, b float64) float64 {
	result := a / b
	return result
}

func Multiplication(a, b float64) float64 {
	result := a * b
	return result
}
